# Ecrivez dans le script une fonction perimetre qui prend en entrée un argument r
# et retourne le périmètre d'un cercle de rayon r, c'est-à-dire 2 * pi * r
# (pi est un objet R qui contient la valeur de pi).
# Tapez ensuite submit() dans la console pour soumettre votre script.

perimetre <- function(r) {
  resultat <- 2 * pi * r
  return(resultat)
}